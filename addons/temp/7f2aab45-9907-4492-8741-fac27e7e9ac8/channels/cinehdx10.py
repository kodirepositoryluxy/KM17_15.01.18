def episodios(item):
    logger.info("streamondemand.channels.cinehdx10 episodios")

    itemlist = []

    data = httptools.downloadpage(item.url, headers=headers).data
    patron = '<div id="seasons">(.*?)<div id="trailer">'
    bloque = scrapertools.find_single_match(data, patron)

    patron = '<div class="numerando">(.*?)</div>\s*<div class="episodiotitle">\s*<a href="([^"]+)">\s*(.*?)</a>'
    matches = re.compile(patron, re.DOTALL).findall(bloque)

    for scrapedtitle_1, scrapedurl, scrapedtitle_2 in matches:
        scrapedthumbnail = ""
        scrapedplot = ""
        scrapedtitle = item.title
        scrapedtitle_1 = scrapedtitle_1.replace(" ", "")

        itemlist.append(infoSod(
            Item(channel=__channel__,
                 action="findvideos",
                 fulltitle=scrapedtitle,
                 contentType="episode",
                 show=scrapedtitle,
                 title="[COLOR azure]" + scrapedtitle + "[/COLOR]" + " (" + scrapedtitle_1 + ") " + scrapedtitle_2,
                 url=scrapedurl,
                 thumbnail=scrapedthumbnail,
                 plot=scrapedplot,
                 folder=True), tipo='tv'))

    if config.get_library_support() and len(itemlist) != 0:
        itemlist.append(
            Item(channel=__channel__,
                 title="Aggiungi alla libreria",
                 url=item.url,
                 action="add_serie_to_library",
                 extra="episodios",
                 contentType="episode",
                 show=item.show))

    return itemlist

